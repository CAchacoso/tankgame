package Tank;

public abstract class GameSetGet {

    protected int x;
    protected int y;

    public int getX(int x){
        return this.x;
    }

    public int getY(int y){
        return this.y;
    }

    public int getWidth(){
        return this.getWidth();
    }
    public int getHeight(){
        return this.getHeight();
    }


}
